package study.raphas.dia2;

public class Operadores_unarios {
    public static void main(String[] args){
        int numero = 5;
        int negativo = -5;
        System.out.println(numero);
        System.out.println(negativo);
        System.out.println(- negativo);
        
        //incremento

        numero++;
        System.out.println(numero);
        System.out.println(++ numero);
        System.out.println(numero++);

    }
}
